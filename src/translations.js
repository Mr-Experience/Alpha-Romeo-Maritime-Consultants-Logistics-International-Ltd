let currentLang = 'en';

// Function to apply translations
function applyTranslations(lang) {
    const langLabel = lang === 'zh' ? 'ZH' : lang.toUpperCase();

    // Update active state in dropdown
    const currentLangText = document.getElementById('current-lang-text');
    if (currentLangText) currentLangText.innerText = langLabel;

    // Update active class in options
    const langOptions = document.querySelectorAll('.lang-option');
    langOptions.forEach(opt => {
        opt.classList.remove('active');
        if (opt.getAttribute('data-lang') === lang) {
            opt.classList.add('active');
        }
    });

    const elements = document.querySelectorAll('[data-translate]');
    elements.forEach(el => {
        const key = el.getAttribute('data-translate');
        if (translations[lang] && translations[lang][key]) {
            el.innerHTML = translations[lang][key];
        } else if (translations['en'] && translations['en'][key]) {
            // Fallback to English if translation missing
            el.innerHTML = translations['en'][key];
        }
    });

    // Handle Placeholders
    const placeholderElements = document.querySelectorAll('[data-translate-placeholder]');
    placeholderElements.forEach(el => {
        const key = el.getAttribute('data-translate-placeholder');
        if (translations[lang] && translations[lang][key]) {
            el.placeholder = translations[lang][key];
        } else if (translations['en'] && translations['en'][key]) {
            el.placeholder = translations['en'][key];
        }
    });

    // Re-render FAQ content if it exists and functions are available
    if (typeof renderFAQ === 'function') {
        renderFAQ();
    }
}

// Initialize Language
function initLanguage() {
    const savedLang = localStorage.getItem('romeoAlphaLang');
    if (savedLang && translations[savedLang]) {
        currentLang = savedLang;
    } else {
        // Detect device language
        const browserLang = navigator.language || navigator.userLanguage;
        const shortLang = browserLang.split('-')[0];
        if (translations[shortLang]) {
            currentLang = shortLang;
        } else {
            currentLang = 'en'; // Default
        }
    }
    applyTranslations(currentLang);
}

// Setup Event Listeners
document.addEventListener('DOMContentLoaded', () => {
    initLanguage();

    const langSelector = document.getElementById('language-selector');
    const langOptions = document.querySelectorAll('.lang-option');

    if (langSelector) {
        langSelector.addEventListener('click', (e) => {
            e.stopPropagation();
            langSelector.classList.toggle('active');
        });

        document.addEventListener('click', () => {
            langSelector.classList.remove('active');
        });
    }

    langOptions.forEach(option => {
        option.addEventListener('click', (e) => {
            e.stopPropagation();
            const lang = option.getAttribute('data-lang');
            currentLang = lang;
            localStorage.setItem('romeoAlphaLang', lang); // Save preference
            applyTranslations(lang);
            if (langSelector) langSelector.classList.remove('active');
        });
    });
});
